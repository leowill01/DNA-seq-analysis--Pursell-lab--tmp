#!/bin/bash

echo Start Job

################################################################################
# JOB DESCRIPTION
################################################################################
# Uses ANNOVAR to annotate nonhuman variants.


################################################################################
# ARGUMENTS
################################################################################
VARIANT_VCF=$1
OUTFILE_PREFIX=$2


################################################################################
# EXECUTION CODE
################################################################################
perl table_annovar.pl \
"$VARIANT_VCF" \
"mm10db"/ \
--vcfinput \
--outfile "$OUTFILE_PREFIX" \
--buildver "mm10" \
--protocol refGene \
--operation g

################################################################################
# END JOB
################################################################################
echo End Job
